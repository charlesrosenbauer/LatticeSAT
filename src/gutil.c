BoxList initBoxList(int size){
	BoxList ret;
	ret.bs		= malloc(sizeof(Box) * size);
	ret.size	= size;
	ret.fill	= 0;
	return ret;
}


int insideBox(Box b, int x, int y){
	return (x >=  b.x)
	&&     (y >=  b.y)
	&&     (x <= (b.x + b.w))
	&&     (y <= (b.y + b.h));
}


int appendBox(BoxList* bl, Box b){
	if(bl->fill+5 >= bl->size){
		Box* tmp   = bl->bs;
		bl->size  *= 2;
		bl->bs     = malloc(sizeof(Box) * bl->size);
		for(int  i = 0; i < bl->fill; i++) bl->bs[i] = tmp[i];
		free(tmp);
	}
	bl->bs[bl->fill] = b;
	bl->fill++;
	return bl->fill-1;
}


uint32_t checkBox(BoxList bl, int x, int y){
	for(int i = 0; i < bl.fill; i++)
		if(insideBox(bl.bs[i], x, y)) return bl.bs[i].func;
	return 0;
}



void drawBox(uint32_t* px, int h, int w, Box b){
	for(int i = 0; i < b.h; i++){
		int y = i + b.y;
		if((y >= 0) && (y < h)){
			for(int j = 0; j < b.w; j++){
				int x = j + b.x;
				int n = (y * w) + x;
				if((x >= 0) || (x <= w)) px[n] = b.color;
			}
		}
	}
}



void drawText(Img px, Img font, char* text, int len, int x, int y){
	int ix = x;
	for(int i = 0; i < len; i++){
		char c = text[i];
		if(c == '\n'){
			x  = ix;
			y += 12;
		}else if(c == '\v'){
			y += 12;
		}else{
			for(int j = 0; j < 12; j++){
				for(int k = 0; k < 8; k++){
					int dx = x + k;
					int dy = y + j;
					
					int cx = ((c % 16) *  8) + k;
					int cy = ((c / 16) * 12) + j;
					int pi = (dy * px.w)+dx;
					int ci = (cy * font.w)+cx;
					px.pixels[pi] = font.pixels[ci]? 0xffffff : px.pixels[pi];
				}
			}
			x += 8;
		}
	}
}


